//double equal fact
const a = 2;
const b = '2';
if (a == b) { // it only check value
    console.log("It's true");
} else {
    console.log("It's false");
}

//triple equal fact
const a = 2;
const b = '2';
if (a === b) { // it check value and type
    console.log("It's true");
} else {
    console.log("It's false");
}